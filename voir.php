         <h3> L'hippodrome de la Solle</h3>

        <div>Installé dans son écrin de verdure, l’hippodrome de la Solle est l’un des plus anciens de France. 
        Sous le règne de Louis XVI, les ducs d’Artois et de Chartres organisèrent pour la 1ère fois, le 11 novembre 1776, des courses à Fontainebleau, devant le roi venu chasser.<br />
        En 1862, après l’ouverture d’une souscription à la Mairie de Fontainebleau, la Société Hippique Départementale organisait officiellement ses premières épreuves sur l’hippodrome de la Solle.
        Les Travaux (bâtiment, piste de 2400 mètres pour la première fois en France, corde à gauche) avaient été réalisés avec l’aide des chasseurs de la garde.<br />
        L’inauguration eut lieu en présence de l’empereur Napoléon III et de l’impératrice Eugénie le 22 juin 1862 et de 30 000 spectateurs dont 4 000 venus de Paris par le train.<br />
        En 2000, un conseil d’administration très motivé, conscient de ses devoirs, pour assumer la pérennité de l’hippodrome et de ses retombées économiques pour la région, a mis en place un vaste programme de rénovation et d’amélioration des installations existantes : sécurité, tribunes, restaurants…<br /></div>

